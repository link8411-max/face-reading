export function generateFortune(사주정보: {
  띠: string;
  일간오행: string;
  강한오행: string;
  약한오행: string;
  사주: { 년주: string; 월주: string; 일주: string };
  음양: string;
}) {
  const 띠데이터 = fortuneDB.띠[사주정보.띠 as keyof typeof fortuneDB.띠];
  const 년운 = fortuneDB["2026_띠별"][사주정보.띠 as keyof typeof fortuneDB["2026_띠별"]];
  const 강한오행데이터 = fortuneDB.오행[사주정보.강한오행 as keyof typeof fortuneDB.오행];
  const 약한오행데이터 = fortuneDB.오행[사주정보.약한오행 as keyof typeof fortuneDB.오행];
  const 행운 = fortuneDB.행운요소[사주정보.강한오행 as keyof typeof fortuneDB.행운요소];
  const 조언카테고리 = 사주정보.강한오행 as keyof typeof fortuneDB.조언.해야할것;

  // 세부 운세 점수 계산 (띠 기본 rating 기반으로 변동)
  const baseRating = 년운.rating;
  const 세부점수 = {
    재물운: Math.min(5, Math.max(1, baseRating + (사주정보.강한오행 === "토" ? 1 : 0))),
    애정운: Math.min(5, Math.max(1, baseRating + (사주정보.강한오행 === "화" ? 1 : -1))),
    직장운: Math.min(5, Math.max(1, baseRating + (사주정보.강한오행 === "금" ? 0 : 0))),
    건강운: Math.min(5, Math.max(1, baseRating + (사주정보.강한오행 === "수" ? 0 : -1))),
    학업운: Math.min(5, Math.max(1, baseRating + (사주정보.강한오행 === "목" ? 1 : 0))),
  };

  return {
    총운: {
      rating: 년운.rating,
      keyword: 년운.keyword,
      summary: 년운.summary,
    },
    월별운세: {
      상반기: 년운.상반기,
      하반기: 년운.하반기,
      최고의달: 년운.최고의달,
      주의할달: 년운.주의할달,
    },
    세부운세: {
      재물운: {
        rating: 세부점수.재물운,
        description: fortuneDB.세부운세.재물운[세부점수.재물운 as keyof typeof fortuneDB.세부운세.재물운],
      },
      애정운: {
        rating: 세부점수.애정운,
        description: fortuneDB.세부운세.애정운[세부점수.애정운 as keyof typeof fortuneDB.세부운세.애정운],
      },
      직장운: {
        rating: 세부점수.직장운,
        description: fortuneDB.세부운세.직장운[세부점수.직장운 as keyof typeof fortuneDB.세부운세.직장운],
      },
      건강운: {
        rating: 세부점수.건강운,
        description: fortuneDB.세부운세.건강운[세부점수.건강운 as keyof typeof fortuneDB.세부운세.건강운],
      },
      학업운: {
        rating: 세부점수.학업운,
        description: fortuneDB.세부운세.학업운[세부점수.학업운 as keyof typeof fortuneDB.세부운세.학업운],
      },
    },
    사주분석: {
      오행조화: `${강한오행데이터.강할때} ${약한오행데이터.약할때}`,
      "2026년궁합": `${강한오행데이터["2026조언"]}`,
    },
    행운요소: {
      행운의숫자: [...행운.숫자, Math.floor(Math.random() * 9) + 1],
      행운의색상: 행운.색상,
      행운의방위: 행운.방위,
      행운의계절: 행운.계절,
    },
    조언: {
      해야할것: fortuneDB.조언.해야할것[조언카테고리],
      피해야할것: fortuneDB.조언.피해야할것[조언카테고리],
      명심할말: fortuneDB.조언.명심할말[조언카테고리],
    },
  };
}
