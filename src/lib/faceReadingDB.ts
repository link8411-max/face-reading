// ========== 종합 운세 생성 함수 ==========
export function generateFaceReading(features: {
  눈?: string;
  코?: string;
  입?: string;
  얼굴형?: string;
  이마?: string;
  턱?: string;
}): {
  features: { [key: string]: FaceFeatureReading };
  totalScores: {
    재물운: number;
    애정운: number;
    직장운: number;
    건강운: number;
    대인운: number;
  };
  summary: string;
  advice: string;
} {
  const selectedFeatures: { [key: string]: FaceFeatureReading } = {};
  const scores = { 재물운: 0, 애정운: 0, 직장운: 0, 건강운: 0, 대인운: 0 };
  let count = 0;

  // 각 특징별 데이터 수집
  if (features.눈 && faceReadingDB.눈[features.눈 as keyof typeof faceReadingDB.눈]) {
    const feature = faceReadingDB.눈[features.눈 as keyof typeof faceReadingDB.눈];
    selectedFeatures.눈 = feature;
    scores.재물운 += feature.scores.재물운;
    scores.애정운 += feature.scores.애정운;
    scores.직장운 += feature.scores.직장운;
    scores.건강운 += feature.scores.건강운;
    scores.대인운 += feature.scores.대인운;
    count++;
  }

  if (features.코 && faceReadingDB.코[features.코 as keyof typeof faceReadingDB.코]) {
    const feature = faceReadingDB.코[features.코 as keyof typeof faceReadingDB.코];
    selectedFeatures.코 = feature;
    scores.재물운 += feature.scores.재물운;
    scores.애정운 += feature.scores.애정운;
    scores.직장운 += feature.scores.직장운;
    scores.건강운 += feature.scores.건강운;
    scores.대인운 += feature.scores.대인운;
    count++;
  }

  if (features.입 && faceReadingDB.입[features.입 as keyof typeof faceReadingDB.입]) {
    const feature = faceReadingDB.입[features.입 as keyof typeof faceReadingDB.입];
    selectedFeatures.입 = feature;
    scores.재물운 += feature.scores.재물운;
    scores.애정운 += feature.scores.애정운;
    scores.직장운 += feature.scores.직장운;
    scores.건강운 += feature.scores.건강운;
    scores.대인운 += feature.scores.대인운;
    count++;
  }

  if (features.얼굴형 && faceReadingDB.얼굴형[features.얼굴형 as keyof typeof faceReadingDB.얼굴형]) {
    const feature = faceReadingDB.얼굴형[features.얼굴형 as keyof typeof faceReadingDB.얼굴형];
    selectedFeatures.얼굴형 = feature;
    scores.재물운 += feature.scores.재물운;
    scores.애정운 += feature.scores.애정운;
    scores.직장운 += feature.scores.직장운;
    scores.건강운 += feature.scores.건강운;
    scores.대인운 += feature.scores.대인운;
    count++;
  }

  if (features.이마 && faceReadingDB.이마[features.이마 as keyof typeof faceReadingDB.이마]) {
    const feature = faceReadingDB.이마[features.이마 as keyof typeof faceReadingDB.이마];
    selectedFeatures.이마 = feature;
    scores.재물운 += feature.scores.재물운;
    scores.애정운 += feature.scores.애정운;
    scores.직장운 += feature.scores.직장운;
    scores.건강운 += feature.scores.건강운;
    scores.대인운 += feature.scores.대인운;
    count++;
  }

  if (features.턱 && faceReadingDB.턱[features.턱 as keyof typeof faceReadingDB.턱]) {
    const feature = faceReadingDB.턱[features.턱 as keyof typeof faceReadingDB.턱];
    selectedFeatures.턱 = feature;
    scores.재물운 += feature.scores.재물운;
    scores.애정운 += feature.scores.애정운;
    scores.직장운 += feature.scores.직장운;
    scores.건강운 += feature.scores.건강운;
    scores.대인운 += feature.scores.대인운;
    count++;
  }

  // 평균 계산
  const totalScores = {
    재물운: count > 0 ? Math.round((scores.재물운 / count) * 10) / 10 : 0,
    애정운: count > 0 ? Math.round((scores.애정운 / count) * 10) / 10 : 0,
    직장운: count > 0 ? Math.round((scores.직장운 / count) * 10) / 10 : 0,
    건강운: count > 0 ? Math.round((scores.건강운 / count) * 10) / 10 : 0,
    대인운: count > 0 ? Math.round((scores.대인운 / count) * 10) / 10 : 0
  };

  // 종합 요약 생성
  const avgScore = (totalScores.재물운 + totalScores.애정운 + totalScores.직장운 + totalScores.건강운 + totalScores.대인운) / 5;
  let summary = "";

  if (avgScore >= 4.5) {
    summary = "전반적으로 매우 좋은 관상을 지니고 계십니다. 복록이 넘치고 큰 성공을 거둘 수 있는 상입니다. 자신감을 가지고 적극적으로 도전하세요.";
  } else if (avgScore >= 4.0) {
    summary = "좋은 관상입니다. 꾸준한 노력으로 성공을 이룰 수 있으며, 특히 대인관계와 재물운이 뛰어납니다. 기회가 왔을 때 놓치지 마세요.";
  } else if (avgScore >= 3.5) {
    summary = "균형 잡힌 관상입니다. 극단적이지 않고 안정적인 삶을 살아갈 수 있습니다. 성실함과 꾸준함이 당신의 최대 강점입니다.";
  } else if (avgScore >= 3.0) {
    summary = "평범하지만 성실한 관상입니다. 큰 기복 없이 안정적인 삶을 영위할 수 있으며, 노력한 만큼의 결실을 맺게 됩니다.";
  } else {
    summary = "젊어서는 고생할 수 있으나 만년에 복이 있는 상입니다. 인내심을 가지고 꾸준히 노력하면 중년 이후 좋은 결과를 얻을 수 있습니다.";
  }

  // 종합 조언 생성
  const highestScore = Math.max(totalScores.재물운, totalScores.애정운, totalScores.직장운, totalScores.건강운, totalScores.대인운);
  const lowestScore = Math.min(totalScores.재물운, totalScores.애정운, totalScores.직장운, totalScores.건강운, totalScores.대인운);

  let advice = "";
  if (highestScore >= 4.5) {
    if (totalScores.재물운 === highestScore) advice += "재물운이 특히 뛰어나니 투자나 사업에 도전해보세요. ";
    if (totalScores.애정운 === highestScore) advice += "애정운이 매우 좋으니 적극적으로 만남을 가지세요. ";
    if (totalScores.직장운 === highestScore) advice += "직장운이 훌륭하니 승진이나 이직을 고려해보세요. ";
    if (totalScores.대인운 === highestScore) advice += "대인운이 좋으니 인맥 관리에 힘쓰세요. ";
  }

  if (lowestScore <= 3.0) {
    if (totalScores.건강운 === lowestScore) advice += "건강운이 다소 약하니 정기 검진과 운동을 권장합니다. ";
    if (totalScores.재물운 === lowestScore) advice += "재물운이 약하니 무리한 투자는 피하고 저축에 힘쓰세요. ";
    if (totalScores.애정운 === lowestScore) advice += "애정운이 약하니 급하게 서두르지 말고 천천히 인연을 찾으세요. ";
    if (totalScores.직장운 === lowestScore) advice += "직장운이 약하니 현재 직장에서 내실을 다지는 것이 좋습니다. ";
  }

  if (advice === "") {
    advice = "모든 운이 균형 잡혀 있으니 현재의 방향을 유지하며 꾸준히 노력하세요. 중용의 도를 지키는 것이 가장 좋습니다.";
  }

  return {
    features: selectedFeatures,
    totalScores,
    summary,
    advice
  };
}

